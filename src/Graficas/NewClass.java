public class NewClass {

   
    String marca;
    String color;
    int talla;

    public NewClass(String marca, String color, int talla) {
   this.marca = marca;
        this.color = color;
        this.talla = talla;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getTalla() {
        return talla;
    }

    public void setTalla(int talla) {
        this.talla = talla;
    }
  
   
    
    
}



